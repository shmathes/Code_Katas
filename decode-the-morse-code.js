decodeMorse = function(morseCode){
    const codes = morseCode.split(' ')
    let word = '';
    for(let i = 0; i < codes.length; i++)
    {
        if(codes[i] === ''){
            word += ' ';
        }else{
            word += MORSE_CODE[codes[i]];
        }
    }
    return word;
}

